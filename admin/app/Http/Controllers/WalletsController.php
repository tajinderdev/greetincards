    public function storeDeposit(Request $request)
    {
        $request->validate([
            'amount' => 'required|numeric|min:0.01',
        ]);

        $wallet = auth()->user()->wallet;
        $wallet->balance += $request->amount;
        $wallet->save();

        return redirect()->route('wallets.index')->with('success', 'Deposit successful.');
    }

